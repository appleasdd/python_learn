#第二種改變大小寫法:
#img = cv2.resize(img,(0,0),fx=0.5,fy=0.5)#(0,0)為定義原圖像的長寬(不用管)
#fx為原寬需要乘上fx所改變成寬度: 要從1080改成540時就將fx寫為0.5
#fy為原寬需要乘上fy所改變成寬度: 要從1080改成540時就將fy寫為0.5
cv2.imshow("img",img)#前項視窗時間,後項需要秀出的照片
cv2.waitKey(0)#等待時間關閉(0為永恆，單位毫秒)


